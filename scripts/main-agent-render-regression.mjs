const require = createRequire(import.meta.url)

async function startViteServer() {
  const viteEntry = require.resolve('vite', { paths: [frontendDir] })
  const { createServer } = await import(pathToFileURL(viteEntry).href)
  const server = await createServer({
    root: frontendDir,
    server: { host: '127.0.0.1', port, strictPort: true },
    logLevel: 'error',
  })
  await server.listen()
  return server
}

async function launchBrowser() {
  try {
    return await chromium.launch()
  } catch (firstError) {
    for (const channel of ['chrome', 'msedge']) {
      try {
        return await chromium.launch({ channel })
      } catch {}
    }
    throw firstError
  }
}

async function expectVisible(locator, label) {
  if (!(await locator.isVisible())) throw new Error(`${label} should be visible`)
}

async function expectHidden(locator, label) {
  if (await locator.isVisible()) throw new Error(`${label} should be hidden`)
}

async function expectNoVisibleText(root, text, label) {
  const matches = root.getByText(text)
  const count = await matches.count()
  for (let index = 0; index < count; index += 1) {
    if (await matches.nth(index).isVisible()) throw new Error(`${label} should not be visible`)
  }
}

